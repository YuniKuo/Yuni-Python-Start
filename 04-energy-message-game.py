# 04-energy-message-game.py：能量互動填空遊戲 ✨ Yuni 製作

name = input("請輸入你的名字：")

adj_1 = input("請輸入你最近內在的一種狀態或感覺（如：穩定、柔軟）：")
noun = input("請輸入你正在散發的能量或特質（如：光芒、愛、信任、水晶般的透明感）：")
adj_2 = input("請輸入你想成為的樣子或狀態（如：自由、豐盛、踏實）：")
verb = input("你最近正在學習或努力的一個動作是什麼？（如：前進、放下、相信）：")
adj_3 = input("最後，用一個詞來形容你想要擁有的今天吧（如：安心、自在）：")

print("\n以下是屬於你的今日能量訊息：\n")
print(f"{name} 喵～")
print(f"你內心感到 {adj_1}，正在散發出 {noun} 的頻率。")
print(f"你渴望活得更 {adj_2}，而此刻的你正在一步步練習著 {verb}。")
print(f"願你擁有一個充滿 {adj_3} 的今天，慢慢前進，也慢慢接近那個你想成為的樣子喵～")
